let num = prompt('Введіть число 0-9');
if (num == 0) {
    window.alert(')');
} else if (num == 1) {
    window.alert('!');
} else if (num == 2) {
    window.alert('@');
} else if (num == 3) {
    window.alert('#');
} else if (num == 4) {
    window.alert('$');
} else if (num == 5) {
    window.alert('%');
} else if (num == 6) {
    window.alert('^');
} else if (num == 7) {
    window.alert('&')
} else if (num == 8) {
    window.alert('*');
} else if (num == 9) {
    window.alert('(')
} else {
    window.alert('Ми так не домовлялись!!!')
}
